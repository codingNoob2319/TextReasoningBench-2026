import re

def extract_and_clean_label(text: str, possible_labels: list) -> str:
    """
    [Global Parser]
    Extracts the classification label from the model-generated text.
    
    Args:
        text: The raw text generated by the model (may contain 'Final Answer: ...' or a block of analysis).
        possible_labels: List of all possible labels for the current dataset (e.g., ['World', 'Sports', ...]).
        
    Returns:
        The extracted label string (maintaining original case, e.g., 'Sci/Tech');
        If extraction fails, returns "unparsed".
    """
    if not text:
        return "unparsed"
        
    text_lower = text.lower().strip()
    
    label_map = {l.lower(): l for l in possible_labels}
    candidates = sorted(label_map.keys(), key=len, reverse=True)
    
    def match_in_text(target_text):
        for cand in candidates:
            pattern = r'\b' + re.escape(cand) + r'\b'

            if cand == 'sci/tech':
                if re.search(r'sci\s*[\/\-]\s*tech', target_text) or 'science/technology' in target_text:
                    return label_map[cand]
            
            if re.search(pattern, target_text):
                return label_map[cand]
        return None

    if "final answer:" in text_lower:
        part_after = text_lower.split("final answer:")[-1].strip()
        part_after = part_after.rstrip('.').rstrip(']').rstrip('}')
        res = match_in_text(part_after)
        if res: return res
    
    end_window = text_lower[-200:]
    res = match_in_text(end_window)
    if res: return res

    res = match_in_text(text_lower)
    if res: return res
            
    return "unparsed"